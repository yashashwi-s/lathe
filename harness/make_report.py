def final_render_page(pdf, methods, idx, n):
    """Best graded conversion: reference vs candidate render."""
    best = None
    for (method, sample), run_dir in idx.items():
        sp = run_dir / "summary.json"
        d = json.loads(sp.read_text())
        f = d.get("final")
        if f and f.get("compiled") and (best is None or f["overall"] > best[0]):
            best = (f["overall"], method, sample, run_dir)
    if not best:
        return n
    overall, method, sample, run_dir = best
    header(pdf, f"Best conversion so far: {sample}",
           f"{method_label(method)} - overall {100 * overall:.1f}")
    tmp = HERE / "report"
    panels = [("Reference (pdfLaTeX)", render_pdf_page(run_dir / "work" / "reference.pdf", tmp / "best_ref.png"))]
    cand = run_dir / "final_candidate.pdf"
    if cand.exists():
        panels.append(("Final candidate (Typst)", render_pdf_page(cand, tmp / "best_cand.png")))
    diff = run_dir / "final_diagnostics" / "diff_p1.png"
    if diff.exists():
        panels.append(("Raster diff (red=missing, blue=extra)", diff))
    pw = (PAGE_W - 80 - 20 * (len(panels) - 1)) / len(panels)
    for i, (label, path) in enumerate(panels):
        px = 40 + i * (pw + 20)
        pdf.setFillColor(NAVY)
        pdf.roundRect(px, PAGE_H - 92, pw, 18, 3, stroke=0, fill=1)
        pdf.setFillColor(colors.white)
        pdf.setFont("Helvetica-Bold", 8.5)
        pdf.drawString(px + 6, PAGE_H - 87, label)
        img_fit(pdf, path, px, 40, pw, PAGE_H - 140)
    footer(pdf, n)
    pdf.showPage()
    return n + 1


