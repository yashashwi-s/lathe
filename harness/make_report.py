COMPARE_COLS = [("overall", "overall"), ("content", "content"), ("layout", "layout"),
                ("typography", "typo"), ("raster", "raster"), ("pagination", "pagin.")]


def ablation_table(pdf, methods, before, after, x, y):
    rows = agg(methods, CORE)
    rb, ra = rows.get(before), rows.get(after)
    headers = [""] + [c[1] for c in COMPARE_COLS] + ["pass", "$/run", "min/run", "iters"]
    widths = [200] + [52] * len(COMPARE_COLS) + [46, 48, 52, 42]
    pdf.setFillColor(NAVY)
    pdf.rect(x, y - 20, sum(widths), 20, stroke=0, fill=1)
    cx = x
    pdf.setFillColor(colors.white)
    pdf.setFont("Helvetica-Bold", 8.5)
    for h, w in zip(headers, widths):
        pdf.drawString(cx + 5, y - 14, h)
        cx += w
    y -= 20

    def fmt_row(label, r, shade):
        nonlocal y
        pdf.setFillColor(shade)
        pdf.rect(x, y - 22, sum(widths), 22, stroke=0, fill=1)
        vals = [label]
        if r:
            partial = r["n_scored"] < r["n_total"]
            vals += [("(%.1f)" if partial else "%.1f") % (100 * r[k]) if r[k] is not None else "nc"
                     for k, _ in COMPARE_COLS]
            vals += [f"{r['n_pass']}/{r['n_done']}", f"{r['cost']:.2f}" if r["cost"] else "-",
                     f"{r['time']/60:.1f}" if r["time"] else "-",
                     f"{r['iters']:.0f}" if r["iters"] else "-"]
        else:
            vals += ["-"] * (len(COMPARE_COLS) + 4)
        cx2 = x
        for j, (v, w) in enumerate(zip(vals, widths)):
            pdf.setFillColor(TEXT)
            pdf.setFont("Helvetica-Bold" if j == 0 else "Helvetica", 8)
            pdf.drawString(cx2 + 5, y - 15, str(v))
            cx2 += w
        y -= 22

    fmt_row("BEFORE  " + method_label(before), rb, colors.white)
    fmt_row("AFTER   " + method_label(after), ra, LIGHT)
    # delta row
    if rb and ra:
        pdf.setFillColor(colors.HexColor("#eaf5f1"))
        pdf.rect(x, y - 22, sum(widths), 22, stroke=0, fill=1)
        cx2 = x
        deltas = ["\u0394"]
        for k, _ in COMPARE_COLS:
            if rb[k] is not None and ra[k] is not None:
                deltas.append(f"{100 * (ra[k] - rb[k]):+.1f}")
            else:
                deltas.append("-")
        deltas += [f"{ra['n_pass'] - rb['n_pass']:+d}",
                   f"{(ra['cost'] or 0) - (rb['cost'] or 0):+.2f}" if ra["cost"] and rb["cost"] else "-",
                   "-", "-"]
        for j, (v, w) in enumerate(zip(deltas, widths)):
            good = j > 0 and str(v).startswith("+")
            pdf.setFillColor(TEAL if good else RED if str(v).startswith("-") and v != "-" else MUTED)
            pdf.setFont("Helvetica-Bold", 8)
            pdf.drawString(cx2 + 5, y - 15, str(v))
            cx2 += w
        y -= 22
    return y


def per_sample_deltas(pdf, methods, before, after, x, y):
    mb, ma = methods.get(before, {}), methods.get(after, {})
    pdf.setFont("Helvetica-Bold", 10)
    pdf.setFillColor(TEXT)
    pdf.drawString(x, y, "Per-sample overall (before -> after):")
    y -= 16
    for s in CORE:
        eb, ea = mb.get(s), ma.get(s)
        vb = 100 * eb["overall"] if eb and not eb.get("failed_compile") else None
        va = 100 * ea["overall"] if ea and not ea.get("failed_compile") else None
        txt = f"{s}:  "
        txt += f"{vb:.1f}" if vb is not None else "nc/-"
        txt += " -> "
        txt += f"{va:.1f}" if va is not None else "nc/-"
        if vb is not None and va is not None:
            txt += f"  ({va - vb:+.1f})"
            col = TEAL if va > vb else RED
        else:
            col = MUTED
        pdf.setFont("Helvetica", 8.5)
        pdf.setFillColor(col)
        pdf.drawString(x + 8, y, txt)
        y -= 13
    return y


def pick_ablation_case(methods, idx, before, after) -> str | None:
    """Largest-gain sample; prefer ones whose AFTER run has a step transcript."""
    mb, ma = methods.get(before, {}), methods.get(after, {})
    candidates = []
    for s in CORE:
        eb, ea = mb.get(s), ma.get(s)
        if not (eb and ea) or eb.get("failed_compile") or ea.get("failed_compile"):
            continue
        run_dir = idx.get((after, s))
        has_transcript = bool(run_dir and (run_dir / "claude_transcript.jsonl").exists())
        candidates.append((has_transcript, ea["overall"] - eb["overall"], s))
    if not candidates:
        return None
    return max(candidates)[2]


