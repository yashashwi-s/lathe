def wrap(pdf, text, x, y, width, size=10, leading=13.5, color=TEXT, font="Helvetica"):
    import textwrap
    pdf.setFont(font, size)
    pdf.setFillColor(color)
    max_chars = max(20, int(width / (size * 0.52)))
    for para in text.split("\n"):
        for line in textwrap.wrap(para, max_chars, break_long_words=False) or [""]:
            pdf.drawString(x, y, line)
            y -= leading
        y -= leading * 0.2
    return y


