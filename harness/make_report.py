def img_fit(pdf, path, x, y, w, h, border=True):
    from PIL import Image
    with Image.open(path) as im:
        r = min(w / im.width, h / im.height)
        dw, dh = im.width * r, im.height * r
    pdf.drawImage(ImageReader(str(path)), x + (w - dw) / 2, y + (h - dh) / 2, dw, dh)
    if border:
        pdf.setStrokeColor(MID)
        pdf.rect(x, y, w, h, stroke=1, fill=0)


def score_color(v):
    return TEAL if v >= 0.7 else AMBER if v >= 0.5 else RED


def method_label(method: str) -> str:
    model, effort, feedback, harness = split_method(method)
    parts = [model, ASCII.get(effort, effort)]
    parts.append(ASCII.get(feedback, feedback))
    if harness != "ŌĆö":
        parts.append(harness)
    return " / ".join(parts)


def run_index() -> dict[tuple[str, str], Path]:
    """(method, sample_id) -> run dir (newest wins)."""
    idx = {}
    for sp in sorted((HERE / "runs").glob("*/summary.json")):
        d = json.loads(sp.read_text())
        idx[(d.get("method", ""), d["sample_id"])] = sp.parent
    return idx


# ---------------------------------------------------------------- transcript
def trajectory(transcript: Path) -> list[dict]:
    """Score checkpoints; each carries the agent's last reasoning text and last edit."""
    steps, edits, reads = [], 0, 0
    last_text, last_edit = "", ""
    for line in transcript.read_text().splitlines():
        try:
            e = json.loads(line)
        except json.JSONDecodeError:
            continue
        if e.get("type") == "assistant":
            for b in e.get("message", {}).get("content", []):
                if b.get("type") == "tool_use":
                    inp = b.get("input", {})
                    if b["name"] in ("Edit", "Write", "MultiEdit"):
                        edits += 1
                        snip = re.sub(r"\s+", " ", str(inp.get("new_string", inp.get("content", ""))))[:80]
                        if snip:
                            last_edit = f"{b['name'].lower()}: \u201c{snip}\u201d"
                    elif b["name"] in ("Read", "Grep"):
                        reads += 1
                elif b.get("type") in ("text", "thinking"):
                    t = re.sub(r"\s+", " ", str(b.get("text") or b.get("thinking") or "").strip())
                    if t:
                        last_text = t
        elif e.get("type") == "user":
            for b in e.get("message", {}).get("content", []) or []:
                if isinstance(b, dict) and b.get("type") == "tool_result":
                    c = b.get("content")
                    if isinstance(c, list):
                        c = " ".join(str(x.get("text", "")) for x in c if isinstance(x, dict))
                    m = re.search(r"overall=([\d.]+)", str(c))
                    if m:
                        steps.append({"overall": float(m.group(1)), "edits": edits, "reads": reads,
                                      "reasoning": last_text, "edit": last_edit})
                        edits = reads = 0
                        last_text, last_edit = "", ""
    return steps


def render_pdf_page(pdf_path: Path, out_png: Path, page=0, dpi=110):
    doc = fitz.open(pdf_path)
    doc[min(page, doc.page_count - 1)].get_pixmap(dpi=dpi).save(out_png)
    return out_png


